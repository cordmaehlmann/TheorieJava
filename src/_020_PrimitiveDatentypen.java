public class _020_PrimitiveDatentypen {

    public static void main(String[] args) {

        // Primitive Datentypen

        // Java ist stark typisiert.
        // Der Datentyp muss bei der Deklaration angegeben werden
        // und kann später nicht geändert werden.

        // Integer - Ganzzahl
        int zahl = 7;
        // zahl = "Hello";         // java: Inkompatible Typen
        System.out.println(zahl);  // 7

        // Double - Fließkommazahl
        double nummer = 3.14;
        System.out.println(nummer);  // 3.14

        // Boolean - Wahrheitswert
        boolean b1 = true;
        System.out.println(b1);  // true

        // Char - Zeichen
        // MUSS in einfachen Anführungszeichen stehen.
        char zeichen = 'A';
        System.out.println(zeichen);

    }
}
